/*eslint-env browser*/
var translateCode = window.prompt ("Please enter a language code");
if (translateCode = 'es') {
    window.document.write("Hello World translated in Esperando is: Saluton mondo");
} else if (translateCode = 'de') {
    window.document.write("Hello World translated in German is: Hallo Welt");
} else if (translateCode = fr) {
    window.document.write("Hello World translated in Franch is: Bonjour le monde");
} else if (translateCode = 'en') {
    window.document.write("Hello World translated in English is: Hello World");
} else  {
    window.document.write("Hello World translated in English is: Hello World");
}



